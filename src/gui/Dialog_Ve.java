    /**
     * Load dữ liệu vé vào dialog
     */
    private void loadData() {
        if (ve == null) return;
        
        // Format cho tiền tệ và ngày giờ
        java.text.NumberFormat currencyFormat = java.text.NumberFormat.getInstance(new java.util.Locale("vi", "VN"));
        java.text.SimpleDateFormat dateFormat = new java.text.SimpleDateFormat("dd/MM/yyyy");
        java.text.SimpleDateFormat timeFormat = new java.text.SimpleDateFormat("HH:mm");
        
        // Mã vé
        lbMaVe.setText(ve.getMaVe() != null ? ve.getMaVe() : "");
        
        // Ga đi, Ga đến
        if (ve.getLichTrinh() != null) {
            lbGaDi.setText(ve.getLichTrinh().getGaDi() != null 
                ? ve.getLichTrinh().getGaDi().getTenGa() 
                : "");
            lbGaDen.setText(ve.getLichTrinh().getGaDen() != null 
                ? ve.getLichTrinh().getGaDen().getTenGa() 
                : "");
            
            // Tàu
            lbTau.setText(ve.getLichTrinh().getChuyenTau() != null 
                ? ve.getLichTrinh().getChuyenTau().getSoHieuTau() 
                : "");
            
            // Ngày đi, Giờ đi
            if (ve.getLichTrinh().getGioKhoiHanh() != null) {
                lbNgayDi.setText(dateFormat.format(
                    java.sql.Timestamp.valueOf(ve.getLichTrinh().getGioKhoiHanh())));
                lbGioDi.setText(timeFormat.format(
                    java.sql.Timestamp.valueOf(ve.getLichTrinh().getGioKhoiHanh())));
            }
        }
        
        // Vị trí toa, vị trí chỗ
        if (ve.getChoNgoi() != null) {
            lbViTriToa.setText(ve.getChoNgoi().getToa() != null 
                ? String.valueOf(ve.getChoNgoi().getToa().getSoToa()) 
                : "");
            lbViTriCho.setText(String.valueOf(ve.getChoNgoi().getViTri()));
        }
        
        // Đối tượng
        lbDoiTuong.setText(ve.getLoaiVe() != null && ve.getLoaiVe().getTenLoaiVe() != null
            ? ve.getLoaiVe().getTenLoaiVe()
            : "");
        
        // Tên khách hàng
        lbTen.setText(ve.getTenKhachHang() != null ? ve.getTenKhachHang() : "");
        
        // CCCD
        lbCccd.setText(ve.getSoCCCD() != null ? ve.getSoCCCD() : "");
        
        // Giá vé
        lbGia.setText(currencyFormat.format(ve.getGiaVe()) + " VNĐ");
        
        // Set dialog ở giữa màn hình
        setLocationRelativeTo(null);
    }

