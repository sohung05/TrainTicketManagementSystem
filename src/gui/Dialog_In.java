    private void inVe(String maHoaDon) {
        try {
            System.out.println("üñ®Ô∏è In v√© t·ª´ h√≥a ƒë∆°n: " + maHoaDon);
            
            // Load danh s√°ch v√© t·ª´ h√≥a ƒë∆°n
            Ve_DAO veDAO = new Ve_DAO();
            List<Ve> danhSachVe = veDAO.findByMaHoaDon(maHoaDon);
            
            if (danhSachVe == null || danhSachVe.isEmpty()) {
                System.err.println("‚ùå Kh√¥ng t√¨m th·∫•y v√© n√†o cho h√≥a ƒë∆°n: " + maHoaDon);
                return;
            }
            
            System.out.println("üìÑ B·∫Øt ƒë·∫ßu in " + danhSachVe.size() + " v√©...");
            
            // In t·ª´ng v√©, m·ªói v√© c√°ch nhau 2 gi√¢y
            for (int i = 0; i < danhSachVe.size(); i++) {
                Ve ve = danhSachVe.get(i);
                
                System.out.println("üñ®Ô∏è In v√© " + (i + 1) + "/" + danhSachVe.size() + ": " + ve.getMaVe());
                boolean success = ThermalPrinter.printTicket(ve);
                
                if (!success) {
                    System.err.println("‚ùå L·ªói khi in v√©: " + ve.getMaVe());
                } else {
                    System.out.println("‚úÖ In v√© th√†nh c√¥ng: " + ve.getMaVe());
                }
                
                // ƒê·ª£i 2 gi√¢y tr∆∞·ªõc khi in v√© ti·∫øp theo (tr·ª´ v√© cu·ªëi c√πng)
                if (i < danhSachVe.size() - 1) {
                    Thread.sleep(2000);
                }
            }
            
            System.out.println("‚úÖ Ho√†n th√†nh in t·∫•t c·∫£ v√©!");
        } catch (Exception e) {
            System.err.println("‚ùå L·ªói khi in v√©: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
