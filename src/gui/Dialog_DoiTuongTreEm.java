    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        // Lấy ngày sinh
        Date ngaySinh = dchNgaySinh.getDate();
        
        if (ngaySinh == null) {
            JOptionPane.showMessageDialog(this,
                "Vui lòng chọn ngày sinh!",
                "Thông báo",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // Chuyển đổi Date sang LocalDate
        LocalDate birthDate = ngaySinh.toInstant()
            .atZone(ZoneId.systemDefault())
            .toLocalDate();
        LocalDate currentDate = LocalDate.now();
        
        // Tính tuổi
        int tuoi = Period.between(birthDate, currentDate).getYears();
        
        // Format ngày sinh để lưu vào "Số giấy tờ"
        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("dd/MM/yyyy");
        ngaySinhResult = sdf.format(ngaySinh);
        
        // Xử lý theo độ tuổi
        if (tuoi >= 10) {
            // >= 10 tuổi → Chuyển sang "Người lớn", xóa ngày sinh
            JOptionPane.showMessageDialog(this,
                "Trẻ từ 10 tuổi trở lên không được mua vé trẻ em!\n" +
                "Vui lòng chọn đối tượng \"Người lớn\".",
                "Cảnh báo",
                JOptionPane.WARNING_MESSAGE);
            
            isConfirmed = false;
            doiTuongResult = "Người lớn"; // Cập nhật đối tượng về Người lớn
            ngaySinhResult = null; // Xóa ngày sinh (để trống)
            dispose();
            
        } else if (tuoi >= 6 && tuoi < 10) {
            // 6-9 tuổi → Vé trẻ em (giảm 25%)
            doiTuongResult = "Trẻ em";
            isConfirmed = true;
            
            // Không hiển thị thông báo, chỉ cập nhật dữ liệu
            dispose();
            
        } else if (tuoi >= 0 && tuoi < 6) {
            // < 6 tuổi → Không cần mua vé, để trống thông tin
            doiTuongResult = ""; // Để trống đối tượng
            isConfirmed = false; // Đánh dấu là không xác nhận
            ngaySinhResult = ""; // Để trống số giấy tờ
            
            // Hiển thị thông báo cho trẻ em dưới 6 tuổi
            JOptionPane.showMessageDialog(this,
                "Trẻ em dưới 6 tuổi không cần mua vé.",
                "Thông báo",
                JOptionPane.INFORMATION_MESSAGE);
            
            dispose();
            
        } else {
            // Ngày sinh không hợp lệ (trong tương lai)
            JOptionPane.showMessageDialog(this,
                "Ngày sinh không hợp lệ!",
                "Lỗi",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnXacNhanActionPerformed

